def render_inventory_text(items: list[dict]) -> str:
    if not items:
        return "Пусто"
    parts = []
    for item in items:
        qty = item.get("quantity", 1)
        description = item.get("description") or ""
        tail = f" — {description}" if description else ""
        parts.append(f"- {item.get('name', 'Безымянный предмет')} x{qty}{tail}")
    return "\n".join(parts)


def render_inventory_inline_text(items: list[dict]) -> str:
    if not items:
        return "пусто"
    return ", ".join(f"{item.get('name', 'Безымянный предмет')} x{item.get('quantity', 1)}" for item in items)


def render_stats_text(stats: dict[str, Any]) -> str:
    if not stats:
        return "нет данных"
    return ", ".join(f"{key}: {value}" for key, value in stats.items())


